package fordowhile;

public class DoWhileExample {
    public static void main(String[] args) {
        int i = 1;
        do {
            System.out.println(i + "Ededimiz hal hazirda");
            i++;
            System.out.println("Kecirik novbeti defe" + i + " ucun yoxlamaga");
        } while (i <= 5);
    }
}
