package Operators;

public class ComparisonOperators {
    public static void main(String[] args) {
        int a = 5;
        int b = 3;
        System.out.println(a == b);
        System.out.println(a != b);
        System.out.println(a < b);
        System.out.println(a > b);
        example();


    }

    public static void example() {
        int c = 4;
        int p = 5;

        System.out.println(c == p);
        System.out.println(c != p);
        System.out.println(c < p);
        System.out.println(c > p);

    }
}
