package Operators;

public class AssingmentsOperators {
    public static void main(String[] args) {
        int i = 5;
        int b = 10;
        System.out.println(b + i);
        System.out.println(b - i);
        System.out.println(b / i);
        System.out.println(b * i);
        System.out.println(b % i);
    }
}
